class ProductModel {
  final String name;
  final String imageUrl;
  final String desc;
  final String price;
  final String type;
  final String id;
  final String departmentId;

  ProductModel({
    required this.name,
    required this.imageUrl,
    required this.desc,
    required this.price,
    required this.type,
    required this.id,
    required this.departmentId,
  });
}
